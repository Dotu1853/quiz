  // Question q1 = Question(q: 'Is DGPC App Development Company?.', a: false);

  @override
  Widget build(BuildContext context) {
    return Column(
      mainAxisAlignment: MainAxisAlignment.spaceBetween,
      crossAxisAlignment: CrossAxisAlignment.stretch,
      children: <Widget>[
        Expanded(
          flex: 5,
          child: Padding(
            padding: EdgeInsets.all(10.0),
            child: Center(
              child: Text(
                quiz.getqustiontext(),
                textAlign: TextAlign.center,
                style: TextStyle(
                  fontSize: 25.0,
